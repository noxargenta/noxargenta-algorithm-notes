void solve() {
    int n,m;
    cin >> n >> m;
    vector<int> a(n+1,1000000009);
    while(m--){
        int l,r,v;
        cin >> l >> r >> v;
        for(int i=l;i<=r;i++){
            a[i]=min(a[i],v);
        }
    }
}

